int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if ((!(s1)) || (!(s2)))
		return (0);
	if (s1[0] != s2[0])
		return (0);
	while (s1[i])
	{
		if (s1[i + 1] != s2[i + 1])
			return (0);
		i++;
	}
	return (1);
}
