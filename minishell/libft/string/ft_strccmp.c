#include "../libft.h"

int     ft_strccmp(char *s1, char *s2, char c)
{
    while (*s1 != '\0' && *s2 != '\0' && *s1  != c && *s2 != c)
    {
        if (*s1 != *s2)
            return (*s1 - *s2);
        s1++;
        s2++;
   }
   return (0);
}
