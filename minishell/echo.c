#include "minishell.h"

void	cmd_echo(char **tab)
{
	int		i;

	i = 1;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		if (tab[++i])
			ft_putstr(" ");
	}
	ft_putendl("");
}
